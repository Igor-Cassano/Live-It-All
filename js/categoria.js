let categoria = location.href;

let len = categoria.length;
categoria = categoria.substring(80, len - 5);

window.addEventListener("load", function() {
  for (let i = 0; i < esperienze.length; i++) {
    if (esperienze[i].type == categoria) {
      const cardContainer = document.getElementById("card-container");
      let article = document.createElement("article");
      article.classList.add("card__article");

      let img = document.createElement("img");
      img.classList.add("card__img");
      img.src = esperienze[i].image;

      let cardData = document.createElement("div");
      cardData.classList.add("card__data");

      let cardLocation = document.createElement("span");
      cardLocation.classList.add("card__location");
      cardLocation.textContent = esperienze[i].location;
      cardData.appendChild(cardLocation);

      let cardTitle = document.createElement("h2");
      cardTitle.classList.add("card__title");
      cardTitle.textContent = esperienze[i].title;
      cardData.appendChild(cardTitle);

      let cardDescription = document.createElement("p");
      cardDescription.classList.add("card__description");
      cardDescription.textContent = esperienze[i].description;
      cardData.appendChild(cardDescription);

      let cardButton = document.createElement("a");
      cardButton.href = "pagamento.html";
      cardButton.classList.add("card__button");
      cardButton.textContent = "Acquista al prezzo di : " + esperienze[i].price;
      cardData.appendChild(cardButton);

      article.appendChild(img);
      article.appendChild(cardData)

      cardContainer.appendChild(article);
    }
  }
});